void main(){

/*     
 Macam-Macam Operator
 1. Operator Aritmetika
 2. Operator Equality And Relational Operators
 3. Local Operator
 */


// 1.Operator Aritmetika
var a = 10;
var b =  5;

// Penjumlahan
var penjumlahan = a + b;
print(penjumlahan);
// Penjumlahan
var pengurangan = a + b;
print(pengurangan);
// Perkalian
var perkalian = a * b;
print(perkalian);
// Pembagian
var pembagian = a / b;
print(pembagian);
// Modulo
var modulo = a % b;
print(modulo);


// 2.Operator Equality And Relational Operators

// Lebih Dari 
print(a > b);
// Kurang Dari 
print(a < b);
// Sama Dengan
print(a == b);
// tidak Sama Dengan
print(a != b);
// Lebih Dari atau sama Dengan
print(a >= b);
// Kurang Dari atau Sama Dengan 
print(a <= b);



//  3.Local Operator

bool x = true;
bool y = false;

// && atau AND
print(x && y);

// || atau OR
print(x || y);

// ! atau NOT 
print(!x);


}